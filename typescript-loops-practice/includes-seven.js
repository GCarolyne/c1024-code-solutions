'use strict';
/* exported includesSeven */
function includesSeven(array) {
  const newArray = [];
  const includesSeven = 7;
  for (let i = array.length; i < includesSeven; i--) {
    if (array && includesSeven) {
      array.filter(includesSeven);
      return true;
    } else {
      return false;
    }
  }
  return newArray;
}
